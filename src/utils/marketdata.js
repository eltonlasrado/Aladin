export function getLot(symbol) {
  return FNO_LOTS[symbol] || FNO_LOTS[(symbol||'').replace('-','')] || 75;
}
export function getStrikeStep(symbol) {
  return FNO_STRIKE_STEP[symbol] || FNO_STRIKE_STEP.DEFAULT;
}
export function getATM(spot, symbol) {
  const step = getStrikeStep(symbol);
  return Math.round(spot / step) * step;
}
